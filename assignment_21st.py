"""Append current date, time in a *file*"""

from datetime import datetime
with open("log.txt","a") as log:
  now = datetime.now()
  log.write("\nDate & Time:"+now.strftime("%d/%m/%Y %I:%M:%S %p"))

with open("log.txt","r") as log:
  print(log.read())

"""Creating folder and saving 3 files in it

"""

import os


Assignments = "Assignments"

if not os.path.exists(Assignments):
    os.mkdir(Assignments)

print(f"Folder '{Assignments}' created successfully.")

import shutil

files=["count.txt","log.txt","loop.txt"]
for file in files:
  shutil.move(file, os.path.join(Assignments, file))

for file in os.listdir(Assignments):
  if file.endswith (".txt"):
    print(file)

