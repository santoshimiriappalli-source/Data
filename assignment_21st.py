with open("loop.txt","w") as f:
  count =0
  while count <5 :
    f.write("first line has Python\n")
    f.write("second line\n")
    f.write("third line has Python\n")
    f.write("fourth line has Python\n")
    f.write("fifth line\n")
    count +=1

with open("loop.txt","r") as f:
  print(f.read())

"""Read lines consisting only "Python"
"""

with open("loop.txt","r") as f:
    for lines in f:
      if "Python" in lines:
        print(lines)

